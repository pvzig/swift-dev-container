        libz3-dev \
        uuid-dev
elif [[ "${version:0:2}" == "10" ]]; then
    check_packages \
        libcurl4 \
        libgcc-5-dev \
        libpython2.7 \
        libsqlite3-0 \
        libstdc++-5-dev \
        libxml2
else
    echo "Unsupported debian version."
fi
